package id.sch.bdg.smkn4.pwpb.biodataapp.aip;

public class Menu {
    int imgMenu;
    String nameMenu;

    public Menu(int imgMenu, String nameMenu) {
        this.imgMenu = imgMenu;
        this.nameMenu = nameMenu;
    }

    public int getImgMenu() {
        return imgMenu;
    }

    public String getNameMenu() {
        return nameMenu;
    }

}

